package com.example.duanaeth.ArrayAdapter;

public class TienIch {
    String id, Ten;
    public TienIch() {

    }

    public TienIch(String id, String ten) {
        this.id = id;
        Ten = ten;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTen() {
        return Ten;
    }

    public void setTen(String ten) {
        Ten = ten;
    }
}
